#ifndef __NEURON_ROUTER_DISPATCH_F0F7_H__
#define __NEURON_ROUTER_DISPATCH_F0F7_H__

#include <stdint.h>
#include <stdbool.h> 

/****************************************************************************\
                            Typedef definitions
\****************************************************************************/
#define NEURON_ROUTER_F0F7_SUCCESS (0x00) // 成功
#define NEURON_ROUTER_F0F7_FORWARD (0x01) // 需要转发

/****************************************************************************\
                           Global functions definitions
\****************************************************************************/
/**
 * @brief F0F7协议路由处理函数
 * @param port_id 端口ID，标识数据来源端口
 * @param data 数据缓冲区指针
 * @param len 数据长度
 * 
 * 功能说明：
 * 根据目标地址决定数据的处理方式：
 * 1. 如果地址是本机地址，则交给本地分发器处理
 * 2. 如果地址是广播地址(0xFF)，则交给本地分发器处理
 * 3. 如果地址是其他设备地址，则转发到对应设备
 */
uint8_t neuron_router_f0f7_route(uint8_t port_id, uint8_t *data, uint16_t len);

#endif /* __NEURON_ROUTER_DISPATCH_F0F7_H__ */
